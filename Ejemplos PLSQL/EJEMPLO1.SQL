VAR B_ID_DEPTO NUMBER(3)

DECLARE
    V_NOMBRE_DEPTO VARCHAR2(50);
    V_CANT_EMP NUMBER(4);
    V_SALARIO_PROM NUMBER(7);
BEGIN
    SELECT D.DEPARTMENT_NAME,  COUNT(E.EMPLOYEE_ID), AVG(E.SALARY)
    INTO V_NOMBRE_DEPTO, V_CANT_EMP, V_SALARIO_PROM
    FROM DEPARTMENTS D
    JOIN EMPLOYEES E ON 
    D.DEPARTMENT_ID = E.DEPARTMENT_ID
    WHERE D.DEPARTMENT_ID =:B_ID_DEPTO
    GROUP BY D.DEPARTMENT_NAME;
    DBMS_OUTPUT.PUT_LINE('Nombre Departamento: '||V_NOMBRE_DEPTO);
    DBMS_OUTPUT.PUT_LINE('Cantidad de empleados: '||V_CANT_EMP);
    DBMS_OUTPUT.PUT_LINE('Salario promedio: '||V_SALARIO_PROM);

END;