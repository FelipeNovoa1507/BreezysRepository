/*
    RECUPERA LOS 3 PRIMEROS EMAILS 



*/
SET SERVEROUTPUT ON;

DECLARE

    TYPE TIPO_TABLA IS TABLE OF EMPLOYEES.EMAIL%TYPE INDEX BY PLS_INTEGER;
    V_TABLA_EMAILS TIPO_TABLA;
    V_CONTADOR NUMBER := 100;
BEGIN
    WHILE V_CONTADOR < 103 LOOP
        SELECT EMAIL INTO V_TABLA_EMAILS(V_CONTADOR)
        FROM EMPLOYEES
        WHERE EMPLOYEE_ID = V_CONTADOR;
        DBMS_OUTPUT.PUT_LINE(V_TABLA_EMAILS(V_CONTADOR));
        V_CONTADOR := V_CONTADOR + 1;
    END LOOP;


END;