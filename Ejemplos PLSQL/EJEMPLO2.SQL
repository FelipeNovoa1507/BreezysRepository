VAR B_ID_EMP NUMBER(3)
DECLARE
    V_FECHA_CON DATE;
    V_NOMBRE VARCHAR2(15);
    V_APELLIDO VARCHAR2(15);
    V_SUELDO NUMBER(5);
    V_ANIOS NUMBER(2);

BEGIN
    SELECT FIRST_NAME, LAST_NAME, HIRE_DATE, SALARY
    INTO V_NOMBRE, V_APELLIDO, V_FECHA_CON, V_SUELDO
    FROM EMPLOYEES
    WHERE EMPLOYEE_ID =:B_ID_EMP;
    V_ANIOS:= EXTRACT(YEAR FROM SYSDATE) - EXTRACT(YEAR FROM V_FECHA_CON);
    
    DBMS_OUTPUT.PUT_LINE('NOMBRE: '||V_NOMBRE||' '||V_APELLIDO);
    DBMS_OUTPUT.PUT_LINE('SUELDO: $'||V_SUELDO);
    DBMS_OUTPUT.PUT_LINE('FECHA CONTRATO: '||V_FECHA_CON);
    DBMS_OUTPUT.PUT_LINE('AÃ‘OS TRABAJADOS: '||V_ANIOS);
    IF V_ANIOS>6 THEN
        DBMS_OUTPUT.PUT_LINE('CORRESPONDE BONO DE $'||V_SUELDO*0.05);
    ELSE
        DBMS_OUTPUT.PUT_LINE('CORRESPONDE BONO DE $0');
    END IF;


END;
